# Construcción del teseracto
def construir_teseracto(fase_step, n_fases=8):
    angulos = np.deg2rad(np.arange(n_fases) * fase_step)
    matriz = np.column_stack([np.cos(angulos), np.sin(angulos)])
    return matriz, angulos

